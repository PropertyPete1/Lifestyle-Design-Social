// @route   POST /api/posts/schedule
// @desc    Schedule a new post
// @access  Private
router.post('/schedule', authenticateToken, async (req: Request, res: Response) => {
  try {
    await connectToDatabase();
    
    const {
      videoId,
      platform,
      content,
      hashtags = [],
      scheduledTime,
      autoGenerated = false,
      contentType = 'video',
      musicUsed,
      thumbnailUsed
    } = req.body;

    const userId = req.user!.id;

    // Validate required fields
    if (!videoId || !platform || !content || !scheduledTime) {
      return res.status(400).json({
        error: 'Missing required fields: videoId, platform, content, scheduledTime'
      });
    }

    // Verify video exists and belongs to user
    const video = await Video.findOne({ _id: videoId, userId });
    if (!video) {
      return res.status(404).json({ error: 'Video not found or access denied' });
    }

    // Create new post
    const newPost = new Post({
      userId,
      videoId,
      platform,
      content,
      hashtags: Array.isArray(hashtags) ? hashtags : [],
      scheduledTime: new Date(scheduledTime),
      autoGenerated,
      contentType,
      musicUsed,
      thumbnailUsed
    });

    const savedPost = await newPost.save();

    return res.status(201).json({
      message: 'Post scheduled successfully',
      post: savedPost
        });
  } catch (error) {
    logger.error('Error scheduling post:', error);
    return res.status(500).json({ error: 'Failed to schedule post' });
  }
});

