// @route   POST /api/posts/auto-schedule
// @desc    Auto-schedule posts for the next few days
// @access  Private
router.post('/auto-schedule', authenticateToken, async (req: Request, res: Response) => {
  try {
    const userId = (req as any).userId;
    const { days = 7, postsPerDay = 3 } = req.body;

    // This would integrate with the smart posting time algorithm
    // For now, we'll create a basic implementation
    const scheduledPosts = [];

    for (let day = 0; day < days; day++) {
      const date = new Date();
      date.setDate(date.getDate() + day);

      for (let post = 0; post < postsPerDay; post++) {
        // Get next video for posting
        const category = post % 2 === 0 ? 'real-estate' : 'cartoon';
        const video = await videoModel.getNextVideoForPosting(userId, category);

        if (video) {
          // Schedule post at default times (9am, 1pm, 6pm)
          const postingTimes = ['09:00', '13:00', '18:00'];
          const scheduledTime = new Date(date);
          const [hours, minutes] = postingTimes[post].split(':');
          scheduledTime.setHours(parseInt(hours), parseInt(minutes), 0, 0);

          const postData = {
            userId,
            videoId: video.id,
            platform: 'instagram',
            content: video.preferredCaption || `Check out this amazing ${video.category} content!`,
            hashtags: video.preferredHashtags || ['realestate', 'homes', 'property'],
            scheduledTime,
            autoGenerated: true,
            contentType: 'video',
            musicUsed: video.preferredMusic,
            thumbnailUsed: video.thumbnailPath,
          };

          const newPost = await postModel.create(postData);
          scheduledPosts.push(newPost);

          // Mark video as posted
          await videoModel.markAsPosted(video.id);
        }
      }
    }

    logger.info(`Auto-scheduled ${scheduledPosts.length} posts for user ${userId}`);

    res.json({
      message: `Auto-scheduled ${scheduledPosts.length} posts`,
      posts: scheduledPosts.map(post => ({
        id: post.id,
        videoId: post.videoId,
        scheduledTime: post.scheduledTime,
        status: post.status,
      })),
    });
  } catch (error) {
    logger.error('Auto-schedule posts error:', error);
    res.status(500).json({ error: 'Server error' });
  }
});

