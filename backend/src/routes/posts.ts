// @route   DELETE /api/posts/:id
// @desc    Cancel/delete post
// @access  Private
router.delete('/:id', authenticateToken, async (req: Request, res: Response) => {
  try {
    const userId = (req as any).userId;
    const postId = req.params.id;

    const post = await postModel.findById(postId);

    if (!post || post.userId !== userId) {
      return res.status(404).json({ error: 'Post not found' });
    }

    // Only allow cancellation of scheduled posts
    if (post.status !== 'scheduled') {
      return res.status(400).json({ error: 'Only scheduled posts can be cancelled' });
    }

    const updatedPost = await postModel.update(postId, {
      status: 'cancelled',
    });

    if (!updatedPost) {
      return res.status(404).json({ error: 'Post not found' });
    }

    logger.info(`Post cancelled: ${updatedPost.id} by user ${userId}`);

    res.json({ message: 'Post cancelled successfully' });
  } catch (error) {
    logger.error('Cancel post error:', error);
    res.status(500).json({ error: 'Server error' });
  }
});

