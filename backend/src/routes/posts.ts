// @route   GET /api/posts/stats
// @desc    Get posting statistics
// @access  Private
router.get('/stats', authenticateToken, async (req: Request, res: Response) => {
  try {
    await connectToDatabase();
    const userId = req.user!.id;
    const { days = 30 } = req.query;

         // Get posting stats using aggregation
     const daysAgo = new Date();
     daysAgo.setDate(daysAgo.getDate() - parseInt(days as string));
     
     const stats = await Post.aggregate([
       { $match: { userId, createdAt: { $gte: daysAgo } } },
       {
         $group: {
           _id: null,
           totalPosts: { $sum: 1 },
           successfulPosts: { $sum: { $cond: [{ $eq: ['$status', 'posted'] }, 1, 0] } },
           failedPosts: { $sum: { $cond: [{ $eq: ['$status', 'failed'] }, 1, 0] } },
           scheduledPosts: { $sum: { $cond: [{ $eq: ['$status', 'scheduled'] }, 1, 0] } },
           instagramPosts: { $sum: { $cond: [{ $eq: ['$platform', 'instagram'] }, 1, 0] } },
           tiktokPosts: { $sum: { $cond: [{ $eq: ['$platform', 'tiktok'] }, 1, 0] } },
           facebookPosts: { $sum: { $cond: [{ $eq: ['$platform', 'facebook'] }, 1, 0] } }
         }
       }
     ]);
     
     const result = stats[0] || {
       totalPosts: 0,
       successfulPosts: 0,
       failedPosts: 0,
       scheduledPosts: 0,
       instagramPosts: 0,
       tiktokPosts: 0,
       facebookPosts: 0
     };

         return res.json({ 
       stats: {
         ...result,
         byPlatform: {
           instagram: result.instagramPosts,
           tiktok: result.tiktokPosts,
           facebook: result.facebookPosts
         }
       }
     });
  } catch (error) {
    logger.error('Get post stats error:', error);
    return res.status(500).json({ error: 'Server error' });
  }
});

