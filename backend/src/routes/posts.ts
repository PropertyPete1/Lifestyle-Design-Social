import { Router, Request, Response } from 'express';
import jwt from 'jsonwebtoken';
import { PostModel } from '../models/Post';
import { VideoModel } from '../models/Video';
import { pool } from '../config/database';
import { logger } from '../utils/logger';

const router = Router();
const postModel = new PostModel(pool);
const videoModel = new VideoModel(pool);

// JWT Secret
const JWT_SECRET = process.env.JWT_SECRET || 'your-secret-key';

// Authentication middleware
const authenticateToken = (req: Request, res: Response, next: Function) => {
  const token = req.headers.authorization?.replace('Bearer ', '');
  
  if (!token) {
    return res.status(401).json({ error: 'No token provided' });
  }

  try {
    const decoded = jwt.verify(token, JWT_SECRET) as { userId: string };
    (req as any).userId = decoded.userId;
    next();
  } catch (error) {
    return res.status(401).json({ error: 'Invalid token' });
  }
};

// @route   POST /api/posts/schedule
// @desc    Schedule a new post
// @access  Private
router.post('/schedule', authenticateToken, async (req: Request, res: Response) => {
  try {
    const userId = (req as any).userId;
    const {
      videoId,
      platform = 'instagram',
      content,
      hashtags = [],
      scheduledTime,
      autoGenerated = true,
      contentType = 'video',
      musicUsed,
      thumbnailUsed,
    } = req.body;

    // Validate video exists and belongs to user
    const video = await videoModel.findById(videoId);
    if (!video || video.userId !== userId) {
      return res.status(404).json({ error: 'Video not found' });
    }

    // Create post
    const post = await postModel.create({
      userId,
      videoId,
      platform,
      content,
      hashtags,
      scheduledTime: new Date(scheduledTime),
      autoGenerated,
      contentType,
      musicUsed,
      thumbnailUsed,
    });

    logger.info(`Post scheduled: ${post.id} for user ${userId}`);

    res.status(201).json({
      message: 'Post scheduled successfully',
      post: {
        id: post.id,
        videoId: post.videoId,
        platform: post.platform,
        content: post.content,
        hashtags: post.hashtags,
        scheduledTime: post.scheduledTime,
        status: post.status,
      }
    });
  } catch (error) {
    logger.error('Schedule post error:', error);
    res.status(500).json({ error: 'Server error' });
  }
});

